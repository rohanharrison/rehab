	<span style="font-size: 200%;color: #da2d29;font-family: 'Palatino Linotype', 'Book Antiqua', Palatino, serif;margin-left: 20px;">I</span>n 2012, the University of Hartford and LIMBS International entered a professional agreement together.  LIMBS International is a non-profit organization that designs, creates and tests new prosthetic devices in their research laboratories in the United States and in clinics throughout the developing world. There are six training centers located in the Dominican Republic, Bolivia, Senegal, India, Kenya and Bangladesh, serving over 23 countries The organization certifies local practitioners on-site to fabricate and fit above-knee and below-knee prosthetic devices. LIMBS International recognized the need for physical therapy clinical expertise in the areas of residual limb care, exercise, and walking activities. The Department of Rehabilitation Sciences teamed up with the Department of Visual Communications Designs at the University of Hartford to design education materials suitable for clinicians and persons with lower extremity limb loss, specifically for use in developing countries. In addition to print versions of the education materials, a patient model and physical therapy student were filmed demonstrating the content of the clinician manual to create supplemental video files/DVD. Currently, the Biomedical Engineering Department at the University of Hartford is also working on developing a prosthetic foot made of burlap.

